package com.example.asm2;

public class ItemMenu {

    private int idIcon;
    private int idText;

    public ItemMenu(int idIcon, int idText) {
        this.idIcon = idIcon;
        this.idText = idText;
    }

    public int getIdIcon() {
        return idIcon;
    }

    public int getIdText() {
        return idText;
    }
}
